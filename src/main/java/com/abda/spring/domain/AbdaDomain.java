package com.abda.spring.domain;

public class AbdaDomain {
	private String retaktId;
	private String fullMap;
	private String retaktTag;
	private String productBaseId;

	public String getRetaktId() {
		return retaktId;
	}

	public void setRetaktId(String retaktId) {
		this.retaktId = retaktId;
	}

	public String getFullMap() {
		return fullMap;
	}

	public void setFullMap(String fullMap) {
		this.fullMap = fullMap;
	}

	public String getRetaktTag() {
		return retaktTag;
	}

	public void setRetaktTag(String retaktTag) {
		this.retaktTag = retaktTag;
	}

	public String getProductBaseId() {
		return productBaseId;
	}

	public void setProductBaseId(String productBaseId) {
		this.productBaseId = productBaseId;
	}

}
